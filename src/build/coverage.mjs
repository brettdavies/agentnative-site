// Coverage page builder — reads the committed coverage-matrix.json artifact
// from src/data/ and produces HTML + markdown for /coverage.
//
// Source of truth: brettdavies/agentnative:coverage/matrix.json
// Sync via: scripts/sync-coverage-matrix.sh

import { readFile } from 'node:fs/promises';
import { PRINCIPLE_NAMES, escHtml } from './util.mjs';

/**
 * Load and validate coverage-matrix.json.
 *
 * @param {string} matrixPath — absolute path to coverage-matrix.json
 * @returns {Promise<object>}
 */
export async function loadCoverageMatrix(matrixPath) {
  const raw = await readFile(matrixPath, 'utf8');
  const matrix = JSON.parse(raw);

  if (!matrix.schema_version || !Array.isArray(matrix.rows) || !matrix.summary) {
    throw new Error('coverage-matrix.json: missing required fields (schema_version, rows, summary)');
  }

  return matrix;
}

/**
 * Build the /coverage page body HTML.
 *
 * Groups requirements by principle, then by level (MUST → SHOULD → MAY).
 * Each row shows the requirement summary, applicability, and verifier check IDs.
 * Empty verifiers render as "UNCOVERED".
 *
 * @param {object} matrix — from loadCoverageMatrix()
 * @returns {string} HTML body
 */
export function buildCoverageBody(matrix) {
  const { rows, summary } = matrix;

  // Group rows by principle number
  const byPrinciple = new Map();
  for (const row of rows) {
    const p = row.principle;
    if (!byPrinciple.has(p)) byPrinciple.set(p, []);
    byPrinciple.get(p).push(row);
  }

  // Level ordering for display
  const levelOrder = { must: 0, should: 1, may: 2 };
  const levelLabel = { must: 'MUST', should: 'SHOULD', may: 'MAY' };

  let html = `<section class="coverage-hero">
  <h1>Spec Coverage Matrix</h1>
  <p class="coverage-hero__lede">Which requirements have automated checks — and which remain uncovered.
  Generated by <code>anc generate coverage-matrix</code> from the
  <a href="https://github.com/brettdavies/agentnative">agentnative</a> principle registry.</p>
</section>

<section class="coverage-summary">
  <h2>Summary</h2>
  <div class="coverage-summary__grid">
    <div class="coverage-stat">
      <span class="coverage-stat__num">${summary.total}</span>
      <span class="coverage-stat__label">total requirements</span>
    </div>
    <div class="coverage-stat coverage-stat--covered">
      <span class="coverage-stat__num">${summary.covered}</span>
      <span class="coverage-stat__label">covered</span>
    </div>
    <div class="coverage-stat coverage-stat--uncovered">
      <span class="coverage-stat__num">${summary.uncovered}</span>
      <span class="coverage-stat__label">uncovered</span>
    </div>
  </div>
  <table class="coverage-level-table" aria-label="Coverage by level">
    <thead>
      <tr>
        <th>Level</th>
        <th>Total</th>
        <th>Covered</th>
        <th>Uncovered</th>
      </tr>
    </thead>
    <tbody>
      <tr>
        <td><strong>MUST</strong></td>
        <td>${summary.must.total}</td>
        <td>${summary.must.covered}</td>
        <td>${summary.must.total - summary.must.covered}</td>
      </tr>
      <tr>
        <td><strong>SHOULD</strong></td>
        <td>${summary.should.total}</td>
        <td>${summary.should.covered}</td>
        <td>${summary.should.total - summary.should.covered}</td>
      </tr>
      <tr>
        <td><strong>MAY</strong></td>
        <td>${summary.may.total}</td>
        <td>${summary.may.covered}</td>
        <td>${summary.may.total - summary.may.covered}</td>
      </tr>
    </tbody>
  </table>
</section>
`;

  // Per-principle sections
  for (const [pNum, pRows] of [...byPrinciple.entries()].sort((a, b) => a[0] - b[0])) {
    const pName = PRINCIPLE_NAMES[`P${pNum}`] || `Principle ${pNum}`;
    const sorted = [...pRows].sort((a, b) => (levelOrder[a.level] ?? 9) - (levelOrder[b.level] ?? 9));

    html += `<section class="coverage-principle">
  <h2><a href="/p${pNum}">P${pNum}: ${escHtml(pName)}</a></h2>
  <table class="coverage-table" aria-label="P${pNum} requirements">
    <thead>
      <tr>
        <th>Level</th>
        <th>Requirement</th>
        <th>Applicability</th>
        <th>Verified by</th>
      </tr>
    </thead>
    <tbody>
`;

    for (const row of sorted) {
      const level = levelLabel[row.level] || row.level.toUpperCase();
      const levelClass = `coverage-level--${row.level}`;

      const applicability =
        row.applicability.kind === 'universal' ? 'Universal' : escHtml(row.applicability.condition || 'Conditional');

      let verifierCell;
      if (row.verifiers.length === 0) {
        verifierCell = '<span class="coverage-uncovered">UNCOVERED</span>';
      } else {
        verifierCell = row.verifiers
          .map((v) => `<code class="coverage-check coverage-check--${escHtml(v.layer)}">${escHtml(v.check_id)}</code>`)
          .join(' ');
      }

      html += `      <tr>
        <td class="${levelClass}">${level}</td>
        <td>${escHtml(row.summary)}</td>
        <td>${applicability}</td>
        <td>${verifierCell}</td>
      </tr>
`;
    }

    html += `    </tbody>
  </table>
</section>
`;
  }

  // Methodology note
  html += `<section class="coverage-methodology">
  <h2>About this matrix</h2>
  <p>This matrix is generated from the <a href="https://github.com/brettdavies/agentnative">agentnative</a> principle
  registry — a flat list of every MUST, SHOULD, and MAY across the seven principles, each mapped to the check IDs
  that verify it. Requirements with no verifiers are marked <strong>UNCOVERED</strong>: the spec requires them but
  <code>anc</code> does not yet test for them.</p>
  <p>"Covered" means a check exists that targets this requirement. "Verified" (on <a href="/scorecards">scorecard pages</a>)
  means the check actually ran for a specific tool. A requirement can be covered in the matrix but unverified for a
  particular tool if the check was skipped (e.g., source checks on a binary-only audit).</p>
</section>`;

  return html;
}

/**
 * Build the /coverage markdown twin.
 *
 * @param {object} matrix — from loadCoverageMatrix()
 * @returns {string} markdown
 */
export function buildCoverageMarkdown(matrix) {
  const { rows, summary } = matrix;
  const levelOrder = { must: 0, should: 1, may: 2 };

  const lines = [
    '# Spec Coverage Matrix',
    '',
    `${summary.total} requirements: ${summary.covered} covered, ${summary.uncovered} uncovered.`,
    '',
    `| Level | Total | Covered | Uncovered |`,
    `|-------|-------|---------|-----------|`,
    `| MUST | ${summary.must.total} | ${summary.must.covered} | ${summary.must.total - summary.must.covered} |`,
    `| SHOULD | ${summary.should.total} | ${summary.should.covered} | ${summary.should.total - summary.should.covered} |`,
    `| MAY | ${summary.may.total} | ${summary.may.covered} | ${summary.may.total - summary.may.covered} |`,
    '',
  ];

  // Group by principle
  const byPrinciple = new Map();
  for (const row of rows) {
    const p = row.principle;
    if (!byPrinciple.has(p)) byPrinciple.set(p, []);
    byPrinciple.get(p).push(row);
  }

  for (const [pNum, pRows] of [...byPrinciple.entries()].sort((a, b) => a[0] - b[0])) {
    const pName = PRINCIPLE_NAMES[`P${pNum}`] || `Principle ${pNum}`;
    const sorted = [...pRows].sort((a, b) => (levelOrder[a.level] ?? 9) - (levelOrder[b.level] ?? 9));

    lines.push(`## [P${pNum}: ${pName}](/p${pNum})`);
    lines.push('');
    lines.push('| Level | Requirement | Applicability | Verified by |');
    lines.push('|-------|-------------|---------------|-------------|');

    for (const row of sorted) {
      const level = row.level.toUpperCase();
      const app = row.applicability.kind === 'universal' ? 'Universal' : row.applicability.condition || 'Conditional';
      const verifiers =
        row.verifiers.length === 0 ? 'UNCOVERED' : row.verifiers.map((v) => `\`${v.check_id}\``).join(', ');
      lines.push(`| ${level} | ${row.summary} | ${app} | ${verifiers} |`);
    }

    lines.push('');
  }

  return lines.join('\n');
}
