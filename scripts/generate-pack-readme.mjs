#!/usr/bin/env bun
// SPDX-License-Identifier: MIT OR Apache-2.0
//
// Generates per-pack README.md companions from Vale rule pack YAMLs.
//
// For each pack under styles/<Pack>/, reads every *.yml, extracts the
// rule definitions, and renders a markdown README listing the literal
// phrases / regex each pack enforces. Pre-push runs this in --check
// mode and refuses to push when a generated README has drifted from
// its source pack.
//
// Usage:
//   bun scripts/generate-pack-readme.mjs            write mode (defaults: Brand, Spec)
//   bun scripts/generate-pack-readme.mjs --check    drift check; exits 1 on diff
//   bun scripts/generate-pack-readme.mjs <pack>...  target specific pack(s)
//
// Exits 0 on success, 1 on drift (--check) or processing error.

import { readFile, readdir, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";
import yaml from "js-yaml";

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const STYLES_DIR = path.join(REPO_ROOT, "styles");
const DEFAULT_PACKS = ["brand", "spec"];
const TRAILER = (pack) =>
  `<!-- generated by scripts/generate-pack-readme.mjs from styles/${pack}/*.yml — do not edit by hand -->`;

function parseArgs(argv) {
  const args = argv.slice(2);
  const check = args.includes("--check");
  const packs = args.filter((a) => !a.startsWith("--"));
  return { mode: check ? "check" : "write", packs: packs.length ? packs : DEFAULT_PACKS };
}

async function readPackRules(packDir) {
  let files;
  try {
    files = (await readdir(packDir)).filter((f) => f.endsWith(".yml")).sort();
  } catch (e) {
    if (e.code === "ENOENT") return [];
    throw e;
  }
  const rules = [];
  for (const file of files) {
    const filePath = path.join(packDir, file);
    const text = await readFile(filePath, "utf8");
    const parsed = yaml.load(text);
    if (!parsed || typeof parsed !== "object") continue;
    const ruleName = path.basename(file, ".yml");
    const sourceMatch = text.match(/^# Source:\s*(.+)$/m);
    const source = sourceMatch ? sourceMatch[1].trim() : null;
    rules.push({ name: ruleName, source, ...parsed });
  }
  return rules;
}

function fmtMessage(msg) {
  if (!msg) return null;
  return msg.replaceAll("'%s'", "`<matched text>`").replaceAll("%s", "`<matched text>`");
}

function wrapParagraph(text, width = 120) {
  const words = text.split(/\s+/);
  const lines = [];
  let line = "";
  for (const word of words) {
    if (!line) {
      line = word;
    } else if (line.length + 1 + word.length <= width) {
      line += " " + word;
    } else {
      lines.push(line);
      line = word;
    }
  }
  if (line) lines.push(line);
  return lines.join("\n");
}

function renderRule(packName, rule) {
  const out = [];
  out.push(`## ${packName}.${rule.name}`, "");
  if (rule.source) out.push(`*Source: ${rule.source}*`, "");
  const message = fmtMessage(rule.message);
  if (message) out.push(wrapParagraph(`**Message:** ${message}`), "");
  if (rule.link) out.push(`**Rationale:** <${rule.link}>`, "");
  if (rule.level) out.push(`**Pack default severity:** \`${rule.level}\` (overridden per-channel via \`.vale.ini\`).`, "");
  if (Array.isArray(rule.tokens) && rule.tokens.length) {
    const sorted = [...rule.tokens].sort();
    const sensitivity = rule.ignorecase === false ? "case-sensitive" : "case-insensitive";
    out.push(`**Enforced tokens** (${sensitivity}, auto-bounded by Vale):`, "");
    for (const t of sorted) out.push(`- \`${t}\``);
    out.push("");
  }
  if (Array.isArray(rule.raw) && rule.raw.length) {
    out.push("**Raw regex** (no auto-boundary):", "");
    for (const r of rule.raw) out.push(`- \`${r}\``);
    out.push("");
  }
  return out;
}

function renderReadme(packName, rules) {
  const lines = [];
  lines.push(`# ${packName} Vale rule pack`, "");
  lines.push(
    wrapParagraph(
      `Auto-generated reference for the rules enforced by \`styles/${packName}/*.yml\`. Each rule's narrative rationale lives in \`BRAND.md\` (universal) or \`.impeccable.md\` (channel). The rule pack is the executable contract for the literal phrases / regex; this README is the human-readable companion.`,
    ),
    "",
  );
  if (!rules.length) {
    lines.push("_No rules in this pack yet._", "");
  } else {
    for (const rule of rules) lines.push(...renderRule(packName, rule));
  }
  lines.push(TRAILER(packName), "");
  return lines.join("\n");
}

async function processPack(packName, mode, errors) {
  const packDir = path.join(STYLES_DIR, packName);
  const readmePath = path.join(packDir, "README.md");
  const rules = await readPackRules(packDir);
  const rendered = renderReadme(packName, rules);

  if (mode === "write") {
    await writeFile(readmePath, rendered, "utf8");
    const n = rules.length;
    console.log(`generate-pack-readme: wrote styles/${packName}/README.md (${n} rule${n === 1 ? "" : "s"})`);
    return;
  }

  let existing = "";
  try {
    existing = await readFile(readmePath, "utf8");
  } catch (e) {
    if (e.code !== "ENOENT") throw e;
  }
  if (existing !== rendered) {
    errors.push(
      `generate-pack-readme: styles/${packName}/README.md has drifted from styles/${packName}/*.yml — run \`bun scripts/generate-pack-readme.mjs\` to regenerate.`,
    );
  }
}

async function main() {
  const { mode, packs } = parseArgs(process.argv);
  const errors = [];
  for (const pack of packs) {
    try {
      await processPack(pack, mode, errors);
    } catch (e) {
      errors.push(`generate-pack-readme: failed to process pack '${pack}': ${e.message}`);
    }
  }
  if (errors.length) {
    for (const e of errors) console.error(e);
    process.exit(1);
  }
}

main();
